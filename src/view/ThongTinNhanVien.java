    public static String convertUpper(String s) {
        String[] words = s.toLowerCase().split("\\s+");
        StringBuilder builder = new StringBuilder();
        for (var item : words) {
            char[] characters = item.toCharArray();
            characters[0] = Character.toUpperCase(characters[0]);
            builder.append(new String(characters));
            builder.append(" ");
        }
       
        return builder.toString().trim();
    }

    private void buttonLuuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonLuuActionPerformed
        // TODO add your handling code here:
        String strTest = textSDT.getText().trim();
        String PASSWORD_PATTERN1 = "[0-9]+";
        String PASSWORD_PATTERN2 = "^.{6,10}$";
        boolean kiemTraSDT1 = Pattern.matches(PASSWORD_PATTERN1, strTest);
        boolean kiemTraSDT2 = Pattern.matches(PASSWORD_PATTERN2, strTest);
        
        String Ho = convertUpper(textHo.getText());
        String Ten = convertUpper(textTen.getText());
        NhanVien nv = new NhanVien();
        nv.setMaNV(Integer.parseInt(textMaNhanVien.getText()));
        nv.setHo(Ho);        
        nv.setTen(Ten);
        nv.setDiaChi(textDiaChi.getText().trim());
        nv.setSdt(textSDT.getText().trim());
        if (kiemTraSDT1 == true && kiemTraSDT2 == true && new Dao_FormNhanVien().updateNHAN_VIEN(nv) == true) {
            JOptionPane.showMessageDialog(rootPane, "Chinh sua thong tin thanh cong!");
        } else {
            JOptionPane.showMessageDialog(rootPane, "Khong the chinh sua thong tin, kiem tra lai so dien thoai! ");
        }
        this.dispose();
    }//GEN-LAST:event_buttonLuuActionPerformed

